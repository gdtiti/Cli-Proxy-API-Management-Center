test('i18n required keys exist in all locales', async () => {
  for (const locale of locales) {
    const filePath = path.join(process.cwd(), locale.file);
    const json = JSON.parse(await fs.readFile(filePath, 'utf8'));

    for (const keyPath of requiredKeys) {
      const value = getKey(json, keyPath);
      assert.ok(value !== undefined, `${locale.name} missing key: ${keyPath}`);
      assert.equal(typeof value, 'string', `${locale.name} key must be a string: ${keyPath}`);
      assert.ok(value.trim().length > 0, `${locale.name} key must not be empty: ${keyPath}`);
    }
  }
});
